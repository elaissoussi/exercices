package com.training.boxomatic;

import org.junit.Test;

import static org.junit.Assert.assertEquals;

public class BoxomaticTest {

    @Test
    public void shouldPrintLabelOfBoxPlacement() {
        Boxomatic boxomatic = new Boxomatic(100, 50, 80);
        assertEquals("A1", boxomatic.printLabelForIndex(0));
        assertEquals("B35", boxomatic.printLabelForIndex(134));
        assertEquals("C1", boxomatic.printLabelForIndex(150));
        assertEquals("C20", boxomatic.printLabelForIndex(169));
        assertEquals("", boxomatic.printLabelForIndex(250));
    }

}
